using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace Admissions.EventArguments
{
    public class AdmissionStudentEventArgs : EventArgs
    {
        public AdmissionStudentEventArgs(string stuno)
        {
            this.stuno = stuno;
        }

        string stuno;

        public string Stuno
        {
            get { return stuno; }
            set { stuno = value; }
        }
    }
}
